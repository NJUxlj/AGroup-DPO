def generate_faq(total: int = 3000) -> list[dict]:
    """生成 FAQ 数据。"""
    items = []
    seen = set()
    rounds = 0
    max_rounds = total * 5  # safety limit (increased for diversity)

    while len(items) < total and rounds < max_rounds:
        rounds += 1
        category = random.choice(list(FAQ_QUESTION_TEMPLATES.keys()))
        template = random.choice(FAQ_QUESTION_TEMPLATES[category])
        question = fill_template(template, FILLERS)

        # 去重
        qhash = hashlib.md5(question.encode()).hexdigest()
        if qhash in seen:
            continue
        seen.add(qhash)

        # 生成答案
        answer = generate_faq_answer(question, category)

        items.append({
            "category": category,
            "question": question,
            "answer": answer,
        })

    random.shuffle(items)
    return items


