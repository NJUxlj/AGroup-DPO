def fill_template(template: str, fillers: dict) -> str:
    """用随机变量填充模板。"""
    result = template
    for key, values in fillers.items():
        placeholder = "{" + key + "}"
        if placeholder in result:
            result = result.replace(placeholder, random.choice(values), 1)
    return result


