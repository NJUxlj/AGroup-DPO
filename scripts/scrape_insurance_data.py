# 主流程
# ---------------------------------------------------------------------------

def ensure_dirs() -> dict[str, Path]:
    """创建输出目录并返回路径。"""
    dirs = {
        "policies": PROJECT_ROOT / "data" / "insurance" / "raw" / "policies",
        "faq": PROJECT_ROOT / "data" / "insurance" / "raw" / "faq",
        "tickets": PROJECT_ROOT / "data" / "insurance" / "raw" / "tickets",
        "output_dpo": PROJECT_ROOT / "data" / "insurance",
    }
    for d in dirs.values():
        d.mkdir(parents=True, exist_ok=True)
    return dirs


def save_policies(dirs: dict[str, Path]) -> str:
    """生成保险条款 JSON 文件（每个 policy 一个独立文件）。"""
    # 合并内置数据 + 网络采集数据
    web_articles = scrape_policy_from_web()

    if web_articles:
        # 将网络采集的条款片段合并为一条记录
        web_policy = {
            "policy_id": "POL-WEB-001",
            "policy_name": "网络采集保险条款集",
            "articles": web_articles,
        }
        all_policies = [web_policy] + INSURANCE_POLICY_TERMS
    else:
        all_policies = list(INSURANCE_POLICY_TERMS)

    # 每个 policy 保存为独立 JSON 文件（流水线期望每个文件一个 policy 对象）
    policy_dir = dirs["policies"]
    for policy in all_policies:
        pid = policy.get("policy_id", "unknown")
        file_path = policy_dir / f"{pid}.json"
        with open(file_path, "w", encoding="utf-8") as f:
            json.dump(policy, f, ensure_ascii=False, indent=2)

    total_articles = sum(len(p.get("articles", [])) for p in all_policies)
    logger.info("保单条款已保存到: %s（%d条记录，%d条条款）", policy_dir, len(all_policies), total_articles)
    return str(policy_dir)


def save_faq(dirs: dict[str, Path]) -> str:
    """生成 FAQ JSON 文件。"""
    web_faq = scrape_faq_from_web()
    all_faq = list(INSURANCE_FAQ) + web_faq

    output_path = dirs["faq"] / "faq_v1.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(all_faq, f, ensure_ascii=False, indent=2)

    logger.info("FAQ已保存: %s（%d条）", output_path, len(all_faq))
    return str(output_path)


def save_tickets(dirs: dict[str, Path]) -> str:
    """生成工单 JSON 文件。"""
    output_path = dirs["tickets"] / "tickets_v1.json"
    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(INSURANCE_TICKETS, f, ensure_ascii=False, indent=2)

    logger.info("工单已保存: %s（%d条）", output_path, len(INSURANCE_TICKETS))
    return str(output_path)


def main():
    """主入口：采集数据 + 保存为流水线期望的格式。"""
    logger.info("=" * 60)
    logger.info("保险业务数据采集脚本启动")
    logger.info("=" * 60)

    dirs = ensure_dirs()

    # 1. 保单条款
    policy_path = save_policies(dirs)

    # 2. FAQ
    faq_path = save_faq(dirs)

    # 3. 工单
    ticket_path = save_tickets(dirs)

    logger.info("=" * 60)
    logger.info("采集完成！")
    logger.info("  保单条款: %s", policy_path)
    logger.info("  FAQ:      %s", faq_path)
    logger.info("  工单:     %s", ticket_path)
    logger.info("=" * 60)

    return {
        "policy_path": policy_path,
        "faq_path": faq_path,
        "ticket_path": ticket_path,
    }


if __name__ == "__main__":
    main()
